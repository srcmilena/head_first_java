package chapter_01;

public class Exerc02_dooBeeDooBeeDo {

    public static void main (String[] args) {
        int x = 1;
        while (x < 5) {
            System.out.print("Doo");
            System.out.print("Bee");
            x = x + 1;
        }
        if (x == 1) {
            System.out.print("Do");
        }
    }
}
